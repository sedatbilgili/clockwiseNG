void StatusController::blink_led(int d, int times)
{
	this->_ledBlinkActive = times > 0;
	this->_ledStateHigh = false;
	this->_ledBlinkIntervalMillis = d > 0 ? static_cast<unsigned long>(d) : 1UL;
	this->_ledLastToggleMillis = millis();
	this->_ledRemainingTransitions = times * 2;
	digitalWrite(ESP32_LED_BUILTIN, LOW);
}

void StatusController::forceRestart()
{
	if (this->_restartScheduled)
	{
		return;
	}

	this->_restartScheduled = true;
	this->_restartAtMillis = millis() + 1000UL;
}

void StatusController::clearStatusAnimation()
{
	if (_statusPixelX >= 0)
	{
		Locator::getDisplay()->drawPixel(_statusPixelX, STATUS_PIXEL_Y, 0x0000);
	}
	_activeStatusScreen = StatusScreen::None;
	resetStatusPixelAnimation();
}

void StatusController::process()
{
	unsigned long now = millis();
	animateStatusPixel(now);

	if (this->_ledBlinkActive && this->_ledRemainingTransitions > 0 && now - this->_ledLastToggleMillis >= this->_ledBlinkIntervalMillis)
	{
		this->_ledLastToggleMillis = now;
		this->_ledStateHigh = !this->_ledStateHigh;
		digitalWrite(ESP32_LED_BUILTIN, this->_ledStateHigh ? HIGH : LOW);
		this->_ledRemainingTransitions--;
		if (this->_ledRemainingTransitions == 0)
		{
			this->_ledBlinkActive = false;
			this->_ledStateHigh = false;
			digitalWrite(ESP32_LED_BUILTIN, LOW);
		}
	}

	if (this->_restartScheduled && now >= this->_restartAtMillis)
	{
		this->_restartScheduled = false;
		ESP.restart();
	}
}
