void StatusController::otaUpdating(uint8_t progress)
{
	const uint8_t bucket = min<uint8_t>(OTA_PROGRESS_BUCKETS, progress / 2);
	if (_lastOtaProgress == bucket)
	{
		return;
	}

	if (_lastOtaProgress == 255)
	{
		_activeStatusScreen = StatusScreen::None;
		resetStatusPixelAnimation();
		Locator::getDisplay()->fillScreen(0x0000);
		printCenter("GUNCELLENIYOR", 42);
	}

	drawOtaIcon(bucket);
	drawOtaProgressBar(bucket);
	_lastOtaProgress = bucket;
}

