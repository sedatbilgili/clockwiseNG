void StatusController::resetStatusPixelAnimation()
{
	_statusPixelLastStepMillis = 0;
	_statusPixelX = -1;
	_statusPixelColorIndex = 0;
}

void StatusController::animateStatusPixel(unsigned long now)
{
	if (_activeStatusScreen == StatusScreen::None)
	{
		return;
	}

	if (_statusPixelLastStepMillis != 0
		&& (now - _statusPixelLastStepMillis) < STATUS_PIXEL_STEP_INTERVAL_MS)
	{
		return;
	}

	_statusPixelLastStepMillis = now;

	if (_statusPixelX >= 0)
	{
		Locator::getDisplay()->drawPixel(_statusPixelX, STATUS_PIXEL_Y, 0x0000);
	}

	if (_statusPixelX < 0)
	{
		_statusPixelX = 0;
	}
	else
	{
		_statusPixelX++;
		if (_statusPixelX >= 64)
		{
			_statusPixelX = 0;
		}
	}

	static const uint16_t colors[] = {0xF800, 0x07E0, 0x001F};
	const uint16_t color = colors[_statusPixelColorIndex % 3];
	Locator::getDisplay()->drawPixel(_statusPixelX, STATUS_PIXEL_Y, color);
	_statusPixelColorIndex = (_statusPixelColorIndex + 1) % 3;
}

void StatusController::drawOtaIcon(uint8_t frameIndex)
{
	Locator::getDisplay()->fillRect(OTA_ICON_BOX_X, OTA_ICON_BOX_Y, OTA_ICON_BOX_SIZE, OTA_ICON_BOX_SIZE, 0x0000);
	const uint8_t rotation = frameIndex % 4;
	for (uint8_t y = 0; y < OTA_ICON_SIZE; ++y)
	{
		for (uint8_t x = 0; x < OTA_ICON_SIZE; ++x)
		{
			const uint16_t color = updateIcon[static_cast<size_t>(y) * OTA_ICON_SIZE + x];
			if (color == _MASK)
			{
				continue;
			}

			uint8_t drawX = x;
			uint8_t drawY = y;
			if (rotation == 1)
			{
				drawX = OTA_ICON_SIZE - 1 - y;
				drawY = x;
			}
			else if (rotation == 2)
			{
				drawX = OTA_ICON_SIZE - 1 - x;
				drawY = OTA_ICON_SIZE - 1 - y;
			}
			else if (rotation == 3)
			{
				drawX = y;
				drawY = OTA_ICON_SIZE - 1 - x;
			}

			Locator::getDisplay()->drawPixel(OTA_ICON_BOX_X + drawX, OTA_ICON_BOX_Y + drawY, color);
		}
	}
}

void StatusController::drawOtaProgressBar(uint8_t bucket)
{
	const uint8_t boundedBucket = min<uint8_t>(OTA_PROGRESS_BUCKETS, bucket);
	const uint8_t filledWidth = (boundedBucket * OTA_PROGRESS_BAR_WIDTH) / OTA_PROGRESS_BUCKETS;
	Locator::getDisplay()->drawRect(
		OTA_PROGRESS_BAR_X - 1,
		OTA_PROGRESS_BAR_Y - 1,
		OTA_PROGRESS_BAR_WIDTH + 2,
		OTA_PROGRESS_BAR_HEIGHT + 2,
		0x7BEF);
	Locator::getDisplay()->fillRect(
		OTA_PROGRESS_BAR_X,
		OTA_PROGRESS_BAR_Y,
		OTA_PROGRESS_BAR_WIDTH,
		OTA_PROGRESS_BAR_HEIGHT,
		0x2104);
	if (filledWidth > 0)
	{
		Locator::getDisplay()->fillRect(
			OTA_PROGRESS_BAR_X,
			OTA_PROGRESS_BAR_Y,
			filledWidth,
			OTA_PROGRESS_BAR_HEIGHT,
			0x07E0);
	}
}

